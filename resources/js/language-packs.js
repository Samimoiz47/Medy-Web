// Language Manager Class
class LanguageManager {
    constructor() {
        this.currentLanguage = 'en';
        this.languagePacks = LanguagePacks;
    }

    setLanguage(languageCode) {
        if (this.languagePacks[languageCode]) {
            this.currentLanguage = languageCode;
            return true;
        }
        return false;
    }

    getCurrentLanguage() {
        return this.languagePacks[this.currentLanguage];
    }

    getMessage(type, key = null) {
        const langPack = this.getCurrentLanguage();
        if (!langPack) return null;

        if (key && langPack[type] && langPack[type][key]) {
            const messages = langPack[type][key];
            return Array.isArray(messages) ? 
                messages[Math.floor(Math.random() * messages.length)] : 
                messages;
        }

        if (langPack[type] && Array.isArray(langPack[type])) {
            const messages = langPack[type];
            return messages[Math.floor(Math.random() * messages.length)];
        }

        return null;
    }

    getVoiceSettings() {
        const langPack = this.getCurrentLanguage();
        return langPack ? langPack.voiceStyles : null;
    }

    getAllLanguages() {
        return Object.keys(this.languagePacks).map(code => ({
            code,
            name: this.languagePacks[code].name
        }));
    }

    getLanguageName(code) {
        return this.languagePacks[code] ? this.languagePacks[code].name : code;
    }
}

// Export for global use
window.LanguageManager = LanguageManager;
window.LanguagePacks = LanguagePacks;
